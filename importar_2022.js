async function importarDatos() {
  console.log("🚀 Iniciando importación de " + sorteos2022.length + " días del 2022...");
  console.log("📦 Esto puede tardar 2-3 minutos, por favor esperá.\n");
  let exitosos = 0;
  let errores = 0;
  const BATCH_SIZE = 400;
  for (let i = 0; i < sorteos2022.length; i += BATCH_SIZE) {
    const lote = db.batch();
    const chunk = sorteos2022.slice(i, i + BATCH_SIZE);
    for (const sorteo of chunk) {
      const docRef = db.collection("sorteos_diaria").doc(sorteo.fecha);
      const [anio, mes, dia] = sorteo.fecha.split("-").map(Number);
      const fechaTimestamp = Timestamp.fromDate(new Date(anio, mes - 1, dia, 0, 0, 0));
      const ahora = Timestamp.now();
      const docData = {
        fecha: fechaTimestamp,
        fecha_key: sorteo.fecha,
        notas: "",
        creado_en: ahora,
      };
      if (sorteo.maniana !== null) docData.numero_maniana = sorteo.maniana;
      if (sorteo.tarde !== null)   docData.numero_tarde   = sorteo.tarde;
      if (sorteo.noche !== null)   docData.numero_noche   = sorteo.noche;
      lote.set(docRef, docData, { merge: true });
    }
    try {
      await lote.commit();
      exitosos += chunk.length;
      const pct = Math.round((exitosos / sorteos2022.length) * 100);
      console.log(`  ✅ ${exitosos}/${sorteos2022.length} días importados (${pct}%)`);
    } catch (error) {
      errores += chunk.length;
      console.error(`  ❌ Error en lote:`, error.message);
    }
    if (i + BATCH_SIZE < sorteos2022.length) {
      await new Promise((r) => setTimeout(r, 500));
    }
  }
  console.log("\n=============================");
  console.log("✅ IMPORTACIÓN 2022 COMPLETADA");
  console.log(`   Días importados:   ${exitosos}`);
  console.log(`   Errores:           ${errores}`);
  console.log(`   Sorteos aprox.:    ~${exitosos * 3}`);
  console.log("=============================\n");
  process.exit(0);
}
