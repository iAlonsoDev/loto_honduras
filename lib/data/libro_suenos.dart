/// Devuelve el significado corto del número
String significadoCorto(int numero) {
  return libroSuenos[numero] ?? '';
}

/// Busca números cuyo significado contenga la palabra clave
List<MapEntry<int, String>> buscarEnLibro(String query) {
  if (query.trim().isEmpty) return libroSuenos.entries.toList();
  final q = query.toLowerCase().trim();
  return libroSuenos.entries
      .where((e) => e.value.toLowerCase().contains(q))
      .toList();
}
